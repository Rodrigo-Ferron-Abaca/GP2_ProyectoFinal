    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        if (jCInstalaciones.getSelectedItem().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un tipo de instalación.");
            return;
        }

        if (jTDetalle.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un detalle.");
            return;
        }

        if (jTPrecio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ingrese un precio.");
            return;
        }

        double precio;
        try {
            precio = Double.parseDouble(jTPrecio.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ingrese un precio valido.");
            return;
        }

        String nombre = jCInstalaciones.getSelectedItem().toString();
        String detalle = jTDetalle.getText();
        boolean estado = jCEstado.isSelected();

        Instalacion inst = new Instalacion();
        inst.setNombre(nombre);
        inst.setDetalleUso(detalle);
        inst.setPrecio30m(precio);
        inst.setEstado(estado);

        InstalacionData id = new InstalacionData();
        id.guardarInstalacion(inst);

        jTCodigo.setText(String.valueOf(inst.getCodInst()));
        limpiarCampos();
        jBModificar.setEnabled(true);
        jBEliminar.setEnabled(true);
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed
        if (jCInstalaciones.getSelectedItem().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un tipo de instalación.");
            return;
        }

        if (jTDetalle.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un detalle.");
            return;
        }

        if (jTPrecio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un precio.");
            return;
        }

        double precio;
        try {
            precio = Double.parseDouble(jTPrecio.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un precio válido.");
            return;
        }

        int cod = Integer.parseInt(jTCodigo.getText());
        String nombre = jCInstalaciones.getSelectedItem().toString();
        String detalle = jTDetalle.getText();
        boolean estado = jCEstado.isSelected();

        Instalacion inst = new Instalacion();
        inst.setCodInst(cod);
        inst.setNombre(nombre);
        inst.setDetalleUso(detalle);
        inst.setPrecio30m(precio);
        inst.setEstado(estado);

        InstalacionData id = new InstalacionData();
        id.modificarInstalacion(inst);
        limpiarCampos();
        jBEliminar.setEnabled(true);
        jBGuardar.setEnabled(false);
    }//GEN-LAST:event_jBModificarActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        try {
            int codigo = Integer.parseInt(jTCodigo.getText());
            iData.borrarInstalacion(codigo);
            cargarTablaGeneral();
            JOptionPane.showMessageDialog(this, "Instalación eliminada definitivamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        try {
            int cod = Integer.parseInt(jTBuscar.getText());

            InstalacionData iData = new InstalacionData();
            Instalacion i = iData.buscarInstalacion(cod);

            if (i == null) {
                return;
            }

            jTCodigo.setText(String.valueOf(i.getCodInst()));
            jTCodigo.setEditable(false);

            jCInstalaciones.setSelectedItem(i.getNombre());
            jTDetalle.setText(i.getDetalleUso());
            jTPrecio.setText(String.valueOf(i.getPrecio30m()));
            jCEstado.setSelected(i.isEstado());

            jBGuardar.setEnabled(false);
            jBModificar.setEnabled(true);
            jBEliminar.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en la búsqueda.");
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

