    private void limpiarTabla() {
        modelo.setRowCount(0);
    }

    private void cargarTablaGeneral() {
        limpiarTabla();
        List<Instalacion> lista = iData.listarInstalacionesActivas();
        for (Instalacion i : lista) {
            modelo.addRow(new Object[]{
                i.getCodInst(),
                i.getNombre(),
                i.getDetalleUso(),
                i.getPrecio30m(),
                i.isEstado() ? "Activa" : "Inactiva"
            });
        }
    }

