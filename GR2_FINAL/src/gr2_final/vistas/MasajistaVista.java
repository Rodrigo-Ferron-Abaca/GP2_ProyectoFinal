    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        try {
            if (txtMatricula.getText().isEmpty()
                    || txtNombreComp.getText().isEmpty()
                    || txtTelefono.getText().isEmpty()
                    || cbEspecialidad.getSelectedIndex() == -1) {

                JOptionPane.showMessageDialog(this,
                        "complete todos los campos y seleccione una especialidad.");
                return;
            }

            int matricula = Integer.parseInt(txtMatricula.getText());
            String nombre = txtNombreComp.getText();
            String telefono = txtTelefono.getText();
            //esto decia yo seleccionar el valor de un item del combobox y pasarlo a string
            String especialidad = cbEspecialidad.getSelectedItem().toString();
            boolean estado = jcheckActivo.isSelected();

            // Crear objeto Masajista
            Masajista m = new Masajista(matricula, nombre, telefono, especialidad, estado);

            MasajistaData md = new MasajistaData();
            md.guardarMasajista(m);

            jBGuardar.setEnabled(false);
            jBModificar.setEnabled(true);
            jBEliminar.setEnabled(true);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "ingrese un numero correcto");
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed

        try {
            if (txtMatricula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Primero debe buscar un masajista por matrícula.");
                return;
            }

            if (txtNombreComp.getText().isEmpty()
                    || txtTelefono.getText().isEmpty()
                    || cbEspecialidad.getSelectedIndex() == -1) {

                JOptionPane.showMessageDialog(this,
                        "Complete todos los campos antes de modificar.");
                return;
            }

            int matricula = Integer.parseInt(txtMatricula.getText());
            String nombre = txtNombreComp.getText();
            String telefono = txtTelefono.getText();
            String especialidad = cbEspecialidad.getSelectedItem().toString();
            boolean estado = jcheckActivo.isSelected();

            Masajista m = new Masajista(matricula, nombre, telefono, especialidad, estado);

            MasajistaData md = new MasajistaData();
            md.modificarMasajista(m);

            JOptionPane.showMessageDialog(this, "Masajista modificado correctamente.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "El campo Matrícula debe ser un número entero.");
        }
    }//GEN-LAST:event_jBModificarActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        try {
            if (txtMatricula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese matrícula.");
                return;
            }

            int mat = Integer.parseInt(txtMatricula.getText());

            int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro?");
            if (confirm != JOptionPane.YES_NO_OPTION) {
                return;
            }

            MasajistaData md = new MasajistaData();
            md.borrarMasajista(mat);

            JOptionPane.showMessageDialog(this, "Masajista eliminado.");

            // Limpiar los campos
            txtMatricula.setText("");
            txtNombreComp.setText("");
            txtTelefono.setText("");
            cbEspecialidad.setSelectedIndex(-1);
            jcheckActivo.setSelected(false);

            jBGuardar.setEnabled(true);
            jBModificar.setEnabled(false);
            jBEliminar.setEnabled(false);

            cargarTablaGeneral();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Matrícula inválida.");
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

