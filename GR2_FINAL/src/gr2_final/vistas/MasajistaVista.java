    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        try {
            // Validar que haya matrícula
            if (txtMatricula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Ingrese un número de matrícula para buscar.");
                return;
            }

            int matricula = Integer.parseInt(txtMatricula.getText());

            MasajistaData md = new MasajistaData();
            Masajista m = md.buscarMasajista(matricula);

            if (m != null) { //m = masajista 
                txtNombreComp.setText(m.getNombreCompleto());
                txtTelefono.setText(m.getTelefono());

                // Seleccionar especialidad en el combo
                cbEspecialidad.setSelectedItem(m.getEspecialidad());

                // Estado activo/inactivo
                jcheckActivo.setSelected(m.isEstado());

                jBGuardar.setEnabled(false);
                jBModificar.setEnabled(true);
                jBEliminar.setEnabled(true);

            } else {
                JOptionPane.showMessageDialog(this,
                        "No existe masajista con esa matrícula.");

                txtNombreComp.setText("");
                txtTelefono.setText("");
                cbEspecialidad.setSelectedIndex(-1);
                jcheckActivo.setSelected(false);

                jBGuardar.setEnabled(true);
                jBModificar.setEnabled(false);
                jBEliminar.setEnabled(false);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "La matrícula debe ser un número entero.");
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

