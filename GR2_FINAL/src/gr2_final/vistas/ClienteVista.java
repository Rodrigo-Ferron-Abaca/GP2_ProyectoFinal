    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        try {
            int dni = Integer.parseInt(jTDniBuscar.getText().trim());

            ClienteData clienteData = new ClienteData();
            Cliente cliente = clienteData.buscarClientePorDni(dni);

            if (cliente == null) {
                JOptionPane.showMessageDialog(this, "No se encontró el cliente");
                return;
            }
            jTCodigo.setText(String.valueOf(cliente.getCodCli()));
            jTDNI.setText(String.valueOf(cliente.getDni()));
            jTNombreCompleto.setText(cliente.getNombreCompleto());
            jTnTelefono.setText(cliente.getTelefono());
            jTEdad.setText(String.valueOf(cliente.getEdad()));
            jTAfecciones.setText(cliente.getAfecciones());
            jCEstado.setSelected(cliente.isEstado());
            //bloquear/desabilitar botones
            jBModificar.setEnabled(true);
            jBEliminar.setEnabled(true);
            jBGuardar.setEnabled(false);
            jBNuevoCliente.setEnabled(true);

            jTCodigo.setEditable(false);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese un DNI válido.");
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed
        try {

            ClienteData clienteData = new ClienteData();

            int codCli = Integer.parseInt(jTCodigo.getText().trim());
            int dni = Integer.parseInt(jTDNI.getText().trim());
            String nombre = jTNombreCompleto.getText().trim();
            String telefono = jTnTelefono.getText().trim();
            int edad = Integer.parseInt(jTEdad.getText().trim());
            String afecciones = jTAfecciones.getText().trim();
            boolean estado = jCEstado.isSelected();

            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre no puede estar vacío.");
                return;
            }
            if (telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El teléfono no puede estar vacío.");
                return;
            }

            Cliente cliente = new Cliente();
            cliente.setCodCli(codCli);
            cliente.setDni(dni);
            cliente.setNombreCompleto(nombre);
            cliente.setTelefono(telefono);
            cliente.setEdad(edad);
            cliente.setAfecciones(afecciones);
            cliente.setEstado(estado);

            clienteData.modificarCliente(cliente);

            jBGuardar.setEnabled(false);
            jBModificar.setEnabled(true);
            jBEliminar.setEnabled(true);
            jBBuscar.setEnabled(true);
            jBNuevoCliente.setEnabled(true);

            jTCodigo.setEditable(false);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Verifique los campos numéricos (Codigo, DNI, Edad).");
        }
    }//GEN-LAST:event_jBModificarActionPerformed

