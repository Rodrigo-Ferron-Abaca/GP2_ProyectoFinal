    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        try {

            if (jTCodigo.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Primero busque un cliente antes de eliminar.");
                return;
            }

            // Obtener el código del cliente
            int codCli = Integer.parseInt(jTCodigo.getText().trim());

            // Confirmación
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "¿Seguro que desea eliminar este cliente?",
                    "Confirmación",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            ClienteData clienteData = new ClienteData();
            clienteData.borradoLogicoCliente(codCli);

            limpiarCampos();

            // nota para los pibes , aca desabilito estos botones pq no podes guardar , modificar , eliminar si los campos estan vacios
            // si podes buscar o crear un cliente nuevo
            jBGuardar.setEnabled(false);
            jBModificar.setEnabled(false);
            jBEliminar.setEnabled(false);
            jBNuevoCliente.setEnabled(true);
            jBBuscar.setEnabled(true);

            jTCodigo.setEditable(false);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Código inválido. Verifique.");
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

    private void jBListarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBListarClientesActionPerformed
        ClienteData cd = new ClienteData();
        List<Cliente> lista = cd.listaClientes();

        borrarFilasTabla();

        for (Cliente c : lista) {
            modelo.addRow(new Object[]{
                c.getCodCli(),
                c.getDni(),
                c.getNombreCompleto(),
                c.getTelefono(),
                c.getEdad(),
                c.getAfecciones(),
                c.isEstado() ? "Activo" : "Inactivo"
            });
        }
    }//GEN-LAST:event_jBListarClientesActionPerformed

